def is_C(bases) :
    return bases == 'C'

def is_G(bases) :
    return bases == 'G'

def is_CG(bases_0, bases_1) :
    return np.logical_and(is_C(bases_0), is_G(bases_1))

def is_GC(bases_0, bases_1) :
    return np.logical_and(is_G(bases_0), is_C(bases_1))

def is_GG(bases_0, bases_1) :
    return np.logical_and(is_G(bases_0), is_G(bases_1))

def is_CC(bases_0, bases_1) :
    return np.logical_and(is_C(bases_0), is_C(bases_1))



class alu_snp_stats_cls(object) :
    cg_snp_dtype = np.dtype([('chrom', np.uint16), ('alu_offset', np.uint16), ('pos', np.uint32), 
                             ('repeat_index', np.uint32), ('ref_cg_0', np.bool), ('ref_cg_1', np.bool),
                             ('alt_cg_0', np.bool), ('alt_cg_1', np.bool)])
    cg_stats_copy_names = ('chrom', 'alu_offset', 'pos', 'repeat_index')
    offset_stats_dtype = np.dtype([('alu_offset', np.uint16), ('count', np.uint32), ('cg_total', np.uint32),
                                   ('ref_c', np.uint32), ('ref_g', np.uint32), ('alt_c', np.uint32),
                                   ('alt_g', np.uint32)])
    
    def __init__(self, alu_snp_data_obj) :
        self.snp_data_obj = alu_snp_data_obj
        self.snp_data = alu_snp_data_obj.snp_data
        self.repeats = alu_snp_data_obj.repeats
        
    def chrom_snp_data(self, chrom) :
        bound = chrom + 1
        inds = self.snp_data['chrom'].searchsorted([chrom, bound])
        return self.snp_data[inds[0]:inds[1]]
    
    def chrom_snp_dna(self, chrom, snp_pos) :
        dna_obj = fsd.dna_rdr_cls(chrom)
        dna = dna_obj.dna
        data_shape = snp_pos.size, 3
        da = np.zeros(data_shape, dtype='S1')
        da[:,1] = dna[snp_pos]
        pos = snp_pos - 1
        da[:,0] = dna[pos]
        pos = snp_pos + 1
        da[:,2] = dna[pos]
        return da
    
    def chrom_cg_stats(self, chrom) :
        sd = self.chrom_snp_data(chrom)
        sd_dna = self.chrom_snp_dna(chrom, sd['pos'])
        cg_stats = np.zeros(sd.size, dtype=self.cg_snp_dtype)
        for name in self.cg_stats_copy_names :
            cg_stats[name] = sd[name]
        self.polarity_cg_stats(cg_stats, sd, sd_dna)
        return cg_stats
    
    def do_cg_stats(self) :
        out_data = []
        for chrom in range(1, 21) :
            out_data.append(self.chrom_cg_stats(chrom))
        self.cg_stats_data = np.concatenate(out_data)
        
    def stats_by_alu_offset(self) :
        self.cg_stats_data.sort(order=['alu_offset', 'repeat_index'])
        offsets, starts, counts = np.unique(self.cg_stats_data['alu_offset'], return_index=True, return_counts=True)
        out_data = []
        for offset, start, count in zip(offsets, starts, counts) :
            bound = start + count
            od = self.cg_stats_data[start:bound]
            ref_c = od['ref_cg_1'].sum()
            ref_g = od['ref_cg_0'].sum()
            alt_c = od['alt_cg_1'].sum()
            alt_g = od['alt_cg_0'].sum()
            cg_total = ref_c + ref_g + alt_c + alt_g
            data = offset, count, cg_total, ref_c, ref_g, alt_c, alt_g
            out_data.append(data)
        self.alu_offset_snp_stats = np.array(out_data, dtype=self.offset_stats_dtype)
        
    def do_stats_work(self) :
        self.do_cg_stats()
        self.stats_by_alu_offset()
        
class pos_alu_snp_stats_cls(alu_snp_stats_cls) :
    
    def __init__(self) :
        data_obj = fsd.pos_alu_snp_data_cls()
        alu_snp_stats_cls.__init__(self, data_obj)
        
    def polarity_cg_stats(self, cg_stats, snp_data, snp_dna) :
        cg_stats['ref_cg_0'] = is_CG(snp_dna[:,0], snp_dna[:,1])
